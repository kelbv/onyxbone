			  <strong>Hello. Today is Monday the 28th of April 2025.</strong><br />
			  <a href="https://kelbv.com/groove/index.php">The Groove Record Charts</a><br /><br />

			  This is a browsable archive of the weekly <strong>Groove Records Hip Hop Sales Charts</strong>,
			  originally printed in <em>Echoes</em> and read out by <strong>Mike Allen</strong> every Friday night on Capital Radio.

			  Groove Records, a tiny shop on Greek Street in Soho, was a key spot for Hip Hop vinyl in 1980s London.
			  The shop's sales chart was broadcast by Mike, often featuring standout tracks and always ending with London's number one.

			  The printed charts appeared in <em>Echoes</em> the following Saturday, usually a week behind the show.

			  This version pulls together all surviving charts from <strong>1984 to mid-1988</strong> into a searchable database.
			  Some charts are missing, if you have scans, get in touch.

			  <br /><br />
			  Original chart scans and data courtesy of <a href="https://www.mikeallencapitalradio.com/mike-allen-capital-radio-home/groove-charts" target="_blank">Simmo</a> - huge thanks for preserving this piece of UK Hip Hop history.
